//Imports necessary hooks and functions from the React library
//createContext: Used to create a Context object, which allows passing data through the component tree without having to pass props down manually at every level.
//useEffect: A hook that lets you perform side effects in function components, such as fetching data or directly interacting with the DOM
//useReducer: A hook used for complex state management. It works similarly to useState but is better suited for managing state logic that involves multiple sub-values or more complex state transitions.

import { createContext, useEffect, useReducer } from "react";

const INITIAL_STATE = {
  user: JSON.parse(localStorage.getItem("user")) || null,
  loading: false,
  error: null,
};

export const AuthContext = createContext(INITIAL_STATE);

const AuthReducer = (state, action) => {
  switch (action.type) {
    case "LOGIN_START":
      return {
        user: null,
        loading: true,
        error: null,
      };
    case "LOGIN_SUCCESS":
      return {
        user: action.payload,
        loading: false,
        error: null,
      };
    case "LOGIN_FAILURE":
      return {
        user: null,
        loading: false,
        error: action.payload,
      };
    case "LOGOUT":
      return {
        user: null,
        loading: false,
        error: null,
      };
    default:
      return state;
  }
};

export const AuthContextProvider = ({ children }) => {
  const [state, dispatch] = useReducer(AuthReducer, INITIAL_STATE);

  useEffect(() => {
    localStorage.setItem("user", JSON.stringify(state.user));
  }, [state.user]);

  return (
    <AuthContext.Provider
      value={{
        user: state.user,
        loading: state.loading,
        error: state.error,
        dispatch,
      }}
    >
      {children}
    </AuthContext.Provider>
  );
};

//This setup effectively manages the authentication state and makes it accessible to any component wrapped by the AuthContextProvider,
// allowing for a centralized and consistent authentication logic throughout the application.